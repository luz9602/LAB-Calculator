// addition
function add(x,y) {
    return parseFloat(x)+parseFloat(y);
}
console.log(add('10',20));

//substraction
function substraction(x,y) {
    return parseFloat(x)-parseFloat(y);
}
console.log(substraction('10',2));


//multiply
function multiply(x,y) {
    return parseFloat(x)*parseFloat(y);
}
console.log(multiply('4',2));

//division
function division(x,y) {
    return parseFloat(x)/parseFloat(y);
}
console.log(division('4',2));